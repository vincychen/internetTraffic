public class Point {
	public double x;
	public double y;
	public Point(double x, double y) {
		this.x =x;
		this.y = y;
	}
	/**
	 * This method is used to get X of the point
	 * @return a double number of x
	 */
	public double getX() {
		return x;
	}
	/**
	 * This method is used to get Y of the point
	 * @return a double number of y
	 */
	public double getY() {
		return y;
	}
}
